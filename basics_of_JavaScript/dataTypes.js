/* String */
let userName1  = "Komal";
console.log(userName1);
console.log(typeof userName1); // "typeof" is used to check what type of data it is 

/* Number */
let age = 99;
console.log(age);
console.log(typeof age);

/* Boolean true/false --> yes/no */
let isProductInCart = false;
//if product is added to cart the isProductInCart is true otherwise it is false
console.log(typeof isProductInCart);

/* undefined */
let userName;
console.log(userName);
console.log(typeof userName);

/* null */
let noValue = null;
console.log(noValue);
console.log(typeof noValue);

/* Object */
// {} it is a keyvalue pair " "
// we declare only object like keyvalue pair
const personDetails = {name: "Komal", age: 22, education: "MCA"}
console.log(typeof personDetails);

// [] it is array 
// we declare only arrays in [] 
const arrayOfNumbers = [1, 2, 3, 4, 5];
console.log(typeof arrayOfNumbers);